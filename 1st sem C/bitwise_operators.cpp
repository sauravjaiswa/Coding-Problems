//Bitwise operator.
#include<stdio.h>
int main()
{
	int a=27;
	int b=39;
	printf("Bitwise operators:");
	printf("\n a & b = %d",a&b);
	printf("\n a | b = %d",a|b);
	printf("\n ~a = %d",~a);
	printf("\n ~b = %d",~b);
	printf("\n a ^ b = %d",a^b);
	printf("\n a << 1 = %d",a<<1);
	printf("\n a >> 1 = %d",b>>1);
}
